    private void calculateForDateReturn(){
        Calendar calendar = new GregorianCalendar();
        
        //Get days, and currentdays + days
        Integer days = Integer.valueOf(jtfDays.getText());
        calendar.add(Calendar.DAY_OF_MONTH,days);
        Date date1 = calendar.getTime();
        
        // set days of returns
        jtfDateofReturns.setText(formaterDMY.format(date1));
    }

