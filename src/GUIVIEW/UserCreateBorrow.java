    private void jtbnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbnCreateActionPerformed
        // TODO add your handling code here:
        try {
            //Get value for creating a borrowed book
            Book b = (Book)cbmb.getSelectedItem();
            Consumator c = (Consumator)ccbm.getSelectedItem();
            Date dateofReceipt = formaterDMY.parse(jtfDateOfReceipt.getText());
            Date dateofReturn = formaterDMY.parse(jtfDateofReturns.getText());
            
            //Set value 
            Borrowedbook bb = new Borrowedbook();
            bb.setBookId(b);
            bb.setConsumatorId(c);
            bb.setDateOfReceipt(dateofReceipt);
            bb.setDateofReturn(dateofReturn);
            
            //Create 
            bbr.create(bb);
            JOptionPane.showMessageDialog(this, "Borrow book created");
            
            //Back to UserMainapge
            JDesktopPane pane = getDesktopPane();
            pane.removeAll();
            pane.add(new UserMainPage()).setVisible(true);
        } catch (CrudFormException ex) {
            ex.getMessage();
        } catch (ParseException ex) {
            ex.getMessage();
        }
    }//GEN-LAST:event_jtbnCreateActionPerformed

    private void jtfDaysKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfDaysKeyPressed
        // TODO add your handling code here:
        //Press enter at jTextFieldDays to calcute the day return
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            calculateForDateReturn();
        }
    }//GEN-LAST:event_jtfDaysKeyPressed


